    private static final String ARG_PARAM2 = "param2";

    // TODO: Rename and change types of parameters
    private String mParam1;
    private String mParam2;


    private InterestDialogFragment.OnDialogFragmentInteractionListener mListener;
    private ImageView close_imageButton;
    public FragmentDetailsDialog() {
        // Required empty public constructor
    }

    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @param param1 Parameter 1.
     * @param param2 Parameter 2.
     * @return A new instance of fragment FragmentDetailsDialog.
     */
    // TODO: Rename and change types and number of parameters
    public static FragmentDetailsDialog newInstance(String param1, String param2) {
        FragmentDetailsDialog fragment = new FragmentDetailsDialog();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        args.putString(ARG_PARAM2, param2);
        fragment.setArguments(args);
        return fragment;
    }



    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
        setStyle(android.support.v4.app.DialogFragment.STYLE_NORMAL, R.style.MY_DIALOG);
    }

    @Override
    public void onStart() {
        super.onStart();
        Dialog d = getDialog();
        if (d!=null){
            int width = ViewGroup.LayoutParams.MATCH_PARENT;
            int height = ViewGroup.LayoutParams.MATCH_PARENT;
            d.getWindow().setLayout(width, height);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment

        View view=inflater.inflate(R.layout.fragment_fragment_details_dialog, container, false);
        getDialog().getWindow().setBackgroundDrawable(new ColorDrawable(0));
        close_imageButton=(ImageView)view.findViewById(R.id.close_imageButton);
        ((HomeActivity)getActivity()).setFragmentDetailsDialog(this);
        view.post(new Runnable() {
            @Override
            public void run() {
                FragmentTransaction fragmentTransaction = getChildFragmentManager().beginTransaction();
                FragmentDialogShows mInterestGenreDialogFragment = new FragmentDialogShows();
                fragmentTransaction.replace(R.id.fragment_details_dialogs, mInterestGenreDialogFragment);
                fragmentTransaction.commit();
            }
        });

        close_imageButton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                getDialog().dismiss();
            }
        });

        return view;
    }



    public void closeDialog() {
        getDialog().dismiss();
    }



